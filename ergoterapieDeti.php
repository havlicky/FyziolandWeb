                    </div>
                    <div class="duvod-proc-nas">
                        <div class="nadpis">Downův syndrom a jiné genetické vady</div>
                        <p>V terapii klientů s&nbsp;Downovým syndromem se soustředíme na mnoho cílů, které vyplývají z&nbsp;individuality každého klienta, terapie je však také zaměřena hlavně na silné stránky klienta. Zaměřujeme se předevnším na motoriku mluvidel, soustředění, vizuoprostorovou a&nbsp;sluchovou analýzu.</p>
                    </div>
                    <div class="duvod-proc-nas">
                        <div class="nadpis">Specifické poruchy učení</div>
                        <p>U dětí se specifickými poruchami učení máme možnost se zaměřit na konkrétní potíže, které dítě trápí. Tím, že multisenzorické prostředí máme možnost upravit tak, abychom eliminovali rušivé vjemy, dítě se tak může lépe na zadaný úkol soustředit a&nbsp;využít svůj potenciál.</p>
                    </div>
                    <div class="duvod-proc-nas">
                        <div class="nadpis">Oslabené zrakové vnímání</div>
                        <p>Cílem terapie ve snoezelenu u dětí s&nbsp;oslabeným zrakovým vnímáním je zlepšení zrakových funkcí, aby tak dítě mohlo bez větších potíži poznávat okolní svět a&nbsp;lépe se v něm orientovat. Jde nám o to, aby se dítě lépe orientovalo ve svém takzvaném MAKROPROSTORU (v&nbsp;lese, ve škole, v&nbsp;domácím prostředí) a&nbsp;v&nbsp;něm zase správně vnímalo svůj MIKROPROSTOR (při hře, svůj pracovní prostor, ponožku během oblékání, písmena při čtení a&nbsp;psaní). Podpora zrakového vnímání má za cíl zlepšit také koordinaci oka - ruky a&nbsp;tuto schopnost potřebujeme na denní bázi.</p>
