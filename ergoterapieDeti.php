                        <div class="nadpis">Poruchy autistického spektra</div>
                        <p>Cílem terapie u&nbsp;klientů na autistickém spektru je rozvoj interakce, komunikace a sociálních dovedností. Vzhledem k&nbsp;tomu, že klienti mívají potíže v&nbsp;oblasti sociálních interakcí, je nezbytné klienta do interakce zapojit a propojit jeho emoce s emocemi jiných lidí. Ve snoezelenu také dochází ke snížení stresu, napětí a strachu z neznámého. Dítě je pak více bezprostřední a iniciativní. Mnohdy se u dětí s PAS objevují poruchy smyslového zpracování, cílem je pak umožnit klientovi jejich regulaci, prožívání vjemů a jejich pochopení. V neposlední řadě nám jde o relaxaci a uvolnění.</p>
                    </div>
                    <div class="duvod-proc-nas">
                        <div class="nadpis">DMO</div>
                        <p>U klientů s&nbsp;dětskou mozkovou obrnou je v&nbsp;první řadě cílem Snoezelen terapie stimulace motorických funkcí. Nesmí být však opomenuta oblast emocí a&nbsp;jejich propojení s&nbsp;pohybovými dovednostmi, respektive je velmi důležité, co klient během pohybu prožívá. Během terapie se zaměřujeme především na silné stránky klienta a&nbsp;jejich další rozvoj.</p>
                    </div>
                    <div class="duvod-proc-nas">
